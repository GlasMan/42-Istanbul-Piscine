#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				j;

	j = 0;
	i = 0;
	if (n == 0)
	{
		return (0);
	}
	while ((*s1 && (*s1 == *s2) && (n - 1) > 0))
	{
		s1++;
		s2++;
		n--;
	}
	return (*s1 - *s2);
}
