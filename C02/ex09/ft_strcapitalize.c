#include <stdio.h>

char	*ft_strcapitalize(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (str[i] != '\0')
	{
		if ((str[i] >= 48 && str[i] <= 57) || (str[i] >= 97 && str[i] <= 122)
			|| (str[i] >= 65 && str[i] <= 90))
		{
			if (j && (str[i] >= 97 && str[i] <= 122))
			{
				str[i] -= 32;
			}
			else if (!j && (str[i] >= 65 && str[i] <= 90))
			{
				str[i] += 32;
			}
			j = 0;
		}
		else
			j = 1;
		i++;
	}
	return (str);
}
