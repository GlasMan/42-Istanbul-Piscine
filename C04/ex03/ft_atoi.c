int	ft_atoi(char *str)
{
	int	neg;
	int	i;
	int	num;

	neg = 1;
	i = 0;
	num = 0;
	while ((str[i] < 14 && str[i] > 8) || str[i] == ' '
		|| str[i] == '-' || str[i] == '+')
		i++;
	while ((str[i] >= '0' && str[i] <= '9'))
	{
		if (str[i - 1] == ' ')
			return (0);
		num = num * 10 + (str[i] - '0');
		i++;
	}
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	return (neg * num);
}
